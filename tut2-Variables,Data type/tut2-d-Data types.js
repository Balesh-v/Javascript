// ----------------------------- Data types ---------------------------------
// ------- 1. String ----------

let fullname = "Balesh varshney";
console.log(fullname)
console.log(typeof fullname)

let car = "";
console.log(car)
console.log(typeof car)

// ------- 2. Number ----------

let age = 19;
console.log(age)
console.log(typeof age)

// ------ 3. BigInt -----------

let digits = BigInt(1234568)
console.log(digits)
console.log(typeof digits)

// ------- 4. Boolean value ---

let com = 1 < 2
console.log(com)
console.log(typeof com)

// ------- 5. Symbol ----------

let name = Symbol("@")
console.log(name)
console.log(typeof name)

// ------ 6. undefined -------

let a ;
console.log(a)
console.log(typeof a)

// ------ 7. null ------------

let b = null;
console.log(b)
console.log(typeof b)

// --------------------------  8. object -----------------------------
// ------ array --------

const arr = ["car" , "name"]
console.log(arr)
console.log(typeof arr)

// ------- object -------

const student = {
    fullname : "balesh varshney"
}
console.log(student.fullname)
console.log(typeof student)