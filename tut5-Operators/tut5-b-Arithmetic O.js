// ------------------- Arithmetic operators --------------------
// Arithmetic operators perform arithmetic on numbers (literals or variables).
// + , - , * , / , ** , % , ++ , --

// ---- Addition ----

let a = 5;
let b = 4 ;
let c = a + b;
console.log(c)

// -- Subtraction ---

let d = a - b;

console.log(d)

// -- Multiplication --

let e = a * b;
console.log(e)

// --- Exponentiation  ---

let f = a ** b;
console.log(f)

// --- Division -----
 
let g = a / b
console.log(g)

// ---- Modulus  ----

let h = a % b
console.log(h)

// ---- Increment ----

let x = 12
let y = x++
console.log(x)

let a1 = 20
let a2 = ++a1
console.log(a1)

// ---- Decrement ---

let x1 = 12
let y1 = x1--
console.log(x1)
console.log(x1)

let a4 = 20
let a3 = --a4
console.log(a4)
console.log(a4)

